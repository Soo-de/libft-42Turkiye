#include "libft.h"

char	*str_cpy(const char *str, char *trim, size_t start, size_t len)
{
	size_t	idx;

	idx = 0;
	while (idx < len && str[start + idx])
	{
		trim[idx] = str[start + idx];
		idx++;
	}
	trim[idx] = '\0';
	return (trim);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	size_t	len;
	char	*trimmed;

	if (!s1 || !set)
		return (NULL);
	end = ft_strlen(s1);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	len = end - start;
	trimmed = ft_calloc(sizeof(char), len + 1);
	if (!trimmed)
		return (NULL);
	str_cpy(s1, trimmed, start, len);
	return (trimmed);
}
